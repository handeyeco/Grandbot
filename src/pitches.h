static uint16_t getPitchByNote(uint16_t note) {
    for (int i = 0; i < 88; ++i) {
        if (noteToPitchMap[i][0] == note) {
            return noteToPitchMap[i][1];
        }
    }

    return 0;
}

static uint16_t getNoteByPitch(uint16_t pitch) {
    for (int i = 0; i < 88; ++i) {
        if (noteToPitchMap[i][1] == pitch) {
            return noteToPitchMap[i][0];
        }
    }

    return 0;
}

